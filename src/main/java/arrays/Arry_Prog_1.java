package arrays;

public class Arry_Prog_1 {
    public static void main(String[] args) {
        int[] ar = new int[3];
        ar[0] = 11;
        ar[1] = 22;
        ar[2] = 33;
        System.out.println(ar);
        System.out.println(ar[0]);
        System.out.println(ar[1]);
        System.out.println(ar[2]);
    }
}

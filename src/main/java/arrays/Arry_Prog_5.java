package arrays;

public class Arry_Prog_5 {
    public static void main(String[] args)
    {
        int[] ar= {11,22,33,44,55,66,77,88,99,111,222,333,444,555,666,777,888,999};

        int s=ar.length;
        System.out.println(s);
        System.out.println("-----------");

        for(int i=0;i<s;i++)
        {
            System.out.println(ar[i]);
        }

    }
}
